-- Create Users table
CREATE TABLE Users (
    id SERIAL PRIMARY KEY,
    email VARCHAR(255) UNIQUE NOT NULL,
    first_name VARCHAR(255) NOT NULL,
    last_name VARCHAR(255) NOT NULL,
    address TEXT,
    password VARCHAR(255) NOT NULL,
    balance DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    account_number VARCHAR(36) UNIQUE NOT NULL,
    public_name VARCHAR(255) NOT NULL,
    is_seller BOOLEAN NOT NULL DEFAULT FALSE,
    summary TEXT
);

-- Create Categories table first, as Products depends on it
CREATE TABLE Categories (
    id SERIAL PRIMARY KEY,  -- Renamed column to 'id' for consistency
    category_name VARCHAR(255) NOT NULL,
    parent_id INT REFERENCES Categories(id)  -- Supports hierarchical structure for subcategories
);

-- Create Products table, as it depends on Categories and Users
CREATE TABLE Products (
    id SERIAL PRIMARY KEY,
    seller_id INT NOT NULL REFERENCES Users(id),  -- References Users table
    category_id INT REFERENCES Categories(id),  -- References Categories table
    name VARCHAR(255) UNIQUE NOT NULL,
    summary TEXT NOT NULL,
    image_url TEXT NOT NULL,
    price DECIMAL(12,2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    available BOOLEAN DEFAULT TRUE
);

-- Create Purchases table, as it depends on Products and Users
CREATE TABLE Purchases (
    id INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    uid INT NOT NULL REFERENCES Users(id),  -- References Users table
    pid INT NOT NULL REFERENCES Products(id),  -- References Products table
    time_purchased TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT (current_timestamp AT TIME ZONE 'UTC')
);
